<template>
  <div class="modal" v-if="show" @click="closeModal">
    <div @click.stop class="modal-content">
      <slot></slot>
    </div>
  </div>
</template>

<script lang="ts">
import { defineComponent } from "vue";

export default defineComponent({
  props: {
    show: {
      type: Boolean,
    },
  },
  methods: {
    closeModal() {
      this.$emit("update:show", false);
    },
  },
});
</script>

<style scoped>
.modal {
  top: 0;
  bottom: 0;
  right: 0;
  left: 0;
  background: rgba(0, 0, 0, 0.5);
  display: flex;
  position: fixed;
}

.modal-content {
  margin: 100px auto;
  background: white;
  border-radius: 15px;
  height: 200px;
}
</style>
